package br.com.lbot.ignoreme.iteractor.entity;

public class Footer {
    private Long id;
    private String type;
    private String text;

    public Footer() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
